package hotel;

/**
 *
 * @authores Douglas e João
 */
public abstract class Cliente {
    protected int id;
    protected String endereco;
    protected String cidade;
    protected String estado;
    protected String tipo;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getEndereco() {
        return endereco;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    public String getCidade() {
        return cidade;
    }

    public void setCidade(String cidade) {
        this.cidade = cidade;
    }

    public String getEstado() {
        return estado;
    }

    public void setEstado(String estado) {
        this.estado = estado;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }
    
    // Métodos abstratos um que retorn a identificação do cliente de acordo com o tipo
    // e o outro que retorna os dados para listagem de acordo com o tipo também.
    public abstract String getIdentificacao();
    public abstract String getCliente();
    
}
